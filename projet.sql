-- Création des tables dans Oracle (ŕ exécuter avec SQL Developer)

-- Table UTILISATEURS
CREATE TABLE UTILISATEURS (
    ID NUMBER PRIMARY KEY,
    NOM VARCHAR2(100) NOT NULL,
    EMAIL VARCHAR2(100) NOT NULL UNIQUE,
    ROLE VARCHAR2(50),
    STATUT VARCHAR2(20),
    DATE_CREATION DATE DEFAULT SYSDATE
);

-- Création d'une séquence pour auto-incrémentation
CREATE SEQUENCE SEQ_UTILISATEURS
    START WITH 1
    INCREMENT BY 1
    NOCACHE
    NOCYCLE;

-- Trigger pour auto-incrémentation
CREATE OR REPLACE TRIGGER TRG_UTILISATEURS
BEFORE INSERT ON UTILISATEURS
FOR EACH ROW
BEGIN
    IF :NEW.ID IS NULL THEN
        SELECT SEQ_UTILISATEURS.NEXTVAL INTO :NEW.ID FROM DUAL;
    END IF;
END;
/

